const STORAGE_KEY = 'app_locale';
const FOLLOW_SYSTEM_KEY = 'app_locale_follow_system';

function resolveSystemLocale() {
  const language = (uni.getSystemInfoSync?.().language || '').toLowerCase();
  return language.startsWith('en') ? 'en' : 'zh';
}

const i18nState = reactive({
  locale: 'zh',
  followSystem: true
});

function getByPath(obj, path) {
  return path.split('.').reduce((acc, key) => acc && acc[key], obj);
}

export function t(path, params = {}) {
  const dict = messages[i18nState.locale] || messages.zh;
  const fallback = messages.zh;
  let text = getByPath(dict, path) ?? getByPath(fallback, path) ?? path;
  Object.keys(params).forEach((key) => {
    text = String(text).split(`{${key}}`).join(params[key]);
  });
  return text;
}

export function setLocale(locale, options = {}) {
  i18nState.locale = locale === 'en' ? 'en' : 'zh';
  i18nState.followSystem = !!options.followSystem;
  uni.setStorageSync(STORAGE_KEY, i18nState.locale);
  uni.setStorageSync(FOLLOW_SYSTEM_KEY, i18nState.followSystem);
}

export function initLocale() {
  const savedLocale = uni.getStorageSync(STORAGE_KEY);
  const savedFollowSystem = uni.getStorageSync(FOLLOW_SYSTEM_KEY);
  
  // 如果没有保存过任何设置，默认跟随系统
  if (!savedLocale && savedFollowSystem === '') {
    i18nState.locale = resolveSystemLocale();
    i18nState.followSystem = true;
    uni.setStorageSync(FOLLOW_SYSTEM_KEY, true);
    return;
  }
  
  // 如果有明确的 followSystem 设置
  const followSystem = savedFollowSystem === true || savedFollowSystem === 'true';
  
  if (followSystem) {
    i18nState.locale = resolveSystemLocale();
    i18nState.followSystem = true;
  } else {
    // 使用用户手动设置的语言
    i18nState.locale = (savedLocale === 'en' || savedLocale === 'zh') ? savedLocale : 'zh';
    i18nState.followSystem = false;
  }
}

export function useI18n() {
  return {
    locale: i18nState,
    t,
    setLocale,
    initLocale
  };
}
